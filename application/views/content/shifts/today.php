<h1>Shifts Today</h1>
<hr/>
<p>This is the new shift feature. This table will be populated daily with shifts. You can then click the checkmark, logging the hour for that shift, or the X which will delete the notification in the table if you did not work that shift.</p>
<br />
<p>To get your shifts to show up here, log in as yourself then add your shifts under your department context. You can still log your shifts like normal as well.</p>
