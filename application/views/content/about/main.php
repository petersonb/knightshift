	<h1>About</h1>
	<p>This website is being developed by a current Wartburg student. His
		goal is to provide an easy way for Wartburg administrators to keep
		track of their student employee hours, while gaining experience on web
		development.</p>
	<hr />
	<h3>Overview</h3>
	<p>When using this site, you will log in as an Administrator, Student
		Employee, or Department. The administor accounts can create and manage
		departments. Administrators may control multiple departments.</p>
	<br />
	<p>Employees, once added to a department by an admin, can log their
		hours for that department. Employees may also belong to multiple
		departments.</p>
	<br />
	<p>When an administrator creates a department, they define the
		department name and login name. They also set a username and password.
		This allows the option to log in as a department. When logged in as a
		department, any employee can log their hours from the same page
		without taking the time to log in as themselves.</p>
	<br />
	<p>When it comes to the end of the month, this site uses the same
		template for excel sheets as the current excel time sheets. As an
		administrator, with two clicks you can download a zip file containing
		all of the excel sheets for each employee. These sheets are fully
		filled out and totaled, except for the employee signature. Employees
		will soon also be able to print out their own time sheets.</p>
