<div class="about">
	<h1>Admin Walkthrough</h1>
	<p>Administrator accounts used by those in charge of keeping track of
		student employment hours.</p>
	<hr />
	<h3>As a new Admin</h3>
	<p>As a new administrator, you will first have to register your
		account. You do this by following the links to the admin registration
		page.</p>
	<img src="/images/admin_reg_path.png" alt="Admin register path" />
	<p>You can then create your account. Your email is what will be used as
		your login name. Administrators accounts are not meant to be used by
		students. As a precaution to let students that may try to register as
		an admin know this, there is a confirmation box on the bottom.</p>
	<img src="/images/admin_reg.png" alt="Admin register" />
	<p>Now that you have created your account, you are ready to start
		managing your departments and their employees. If your department is
		already in existance, you will have to ask an existing admin of that
		department to add you to the department.</p>
	<br />
	<p>If your department has not been created on the site yet, you can
		create it by going to the departments dropdown and selecting create.</p>
	<img src="/images/department_create_path.png" alt="x" />
	<p>When creating your department, you want to keep in mind that the
		login name and password are what your employees will log into the
		department section of the site with. If you don't want them to be able
		to do this, don't give them the passowrd. They can still log their
		hours for this department from their own account. The department id is
		what used on the excel sheets.</p>
	<img src="/images/department_create.png" alt="x" />
	<p>Once you have created your department, you will see it in your list
		of departments. When logged in as an administrator (or employee), your
		account can be given the context of one department at a time. This
		helps avoiding confusion if you belong to many departments.</p>
	<br />
	<p>By default when you log in, you have no context. Without a context,
		you can view all hours from every department. If you wish to deal
		specifically with one department, you set your context to that
		department by selecting its name from the departments list. You can
		remove your context by selecting "Unset Context" from the menu bar.</p>
	<img src="/images/current_departments.png" alt="x" />
	<p>Once your department is selected, you are ready to add employees to
		it. In the departments dropdown, select "Add Employee".</p>
	<img src="/images/dep_redirect_add_emp_path.png" alt="x" />
	<p>This page allows you to add existing employees to your department.
		In the table, every existing employee that does not already belong to
		your department is listed. If they do not exist, you can create them
		yourself by a link at the top.</p>
	<br />
	<p>Select the employee by clicking on their name. If you do not see
		them, you can search their first or last name or email in the search
		bar in the top right of the table.</p>
	<br />
	<p>Enter how much they will make per hour for your department in the
		hourly rate field, and click the "Add Employee" button at the bottom.</p>
	<img src="/images/add_emp_select.png" alt="x" />
	<p>Once the employee is added to your department, they will no longer
		be visable in this table.</p>
	<img src="/images/emp_add_after.png" alt="x" />
	<p>Once you have added all of your employees, you will be able to view
		and edit them in the Employees->View All page.</p>
	<img src="/images/view_all_after_add.png" alt="x" />
	<p>Once employees are added to the department, they will be able to log
		their time for that department from their own profile, as well as from
		the department login. You are also able to log, edit, and delete their
		hours from your admin account.</p>
	<br />
	<p>In the hours dropdown, select "Log Time". From here (while in
		context) you can log hours for any of your employees.</p>
	<img src="/images/admin_log_date_sel.png" alt="x" />
	<p>You can view the hours your employees have logged by selecting "View
		All" from the hours dropdown. As an admin, you have the ability to
		edit or delete each time logged.</p>
	<img src="/images/hours_view_all.png" />
	<p>Finally, at the end of the month when it is time to turn in hours,
		you are able to generate all of the excel sheets for every employee in
		your department. All of the hours logged for each employee are put
		into the same excel sheet currently being used by most Wartburg
		workstudy departments. They are then zipped up and ready to be
		downloaded.</p>
	<img src="/images/excel_pre.png" alt="x" />
	<p>Once generated, you will be presented with a download link.</p>
	<img src="/images/excel_post_dwnld.png" alt="x" />
	<p>Save the downloaded .zip file wherever you wish, or if not prompted
		you can most likely find it in your downloads folder. Unzip the file
		by right clicking on it. You can then view and print off every
		employee's time sheet for the month. You can see the sheets below
		generated from the same hours we used above for Chuck, George, and
		Mister Knight. (Right click and view image for bigger display)</p>
	<img style="max-height: 1000px; width: 100%;"
		src="/images/excel_sheets.png" alt="x" />
	<p>There you have it. Thanks for reading my short introduction to this
		application! I'm working on a lot more features including an email and
		messaging system, and scheduling system. Please email me at brett@petersonb.com with any
		questions.</p>
